CREATE TABLE ID_SEQ_TABLE (
    VALUUE BIGINT
);

CREATE TABLE BAND (
    ID BIGINT PRIMARY KEY,
    NAME VARCHAR(20),
    ESTABLISHMENT_DATE DATE
);

CREATE TABLE ARTIST (
    ID BIGINT PRIMARY KEY,
    NAME VARCHAR(20),
    AGE INT,
    INSTRUMENT VARCHAR(20)
);

CREATE TABLE BAND_ARTIST (
    ID BIGINT PRIMARY KEY,
    ARTIST_ID BIGINT CONSTRAINT FK_BA_ARTIST REFERENCES ARTIST,
    BAND_ID BIGINT CONSTRAINT FK_BA_BAND REFERENCES BAND
);

